let b1 = document.querySelector("#separatePointsBtn1");
let p1 = document.querySelector("#fullDescription1");

b1.addEventListener('click', () => {
    if (p1.classList.contains('hidden')) {
        p1.classList.remove('hidden');
        b1.innerText = 'Schowaj tekst';
    }
    else {
        p1.classList.add('hidden');
        b1.innerText = 'Dowiedz się więcej';
    }
})



